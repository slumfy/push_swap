#include "libft.h"

void	ft_putnbr(int n)
{
	long long	nbr;

	nbr = n;
	if (n < 0)
	{
		ft_putchar('-');
		nbr = -nbr;
	}
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		ft_putchar(nbr + 48);
}
